    async function getChunk(a, b) {
        const responses = links.slice(a, b).map(
            async id => {
                let promiseResolve, promiseReject;
                const resp = new Promise(function(resolve, reject) {
                    promiseResolve = resolve;
                    promiseReject = reject;
                })
                const link = 'https://dungeon.su/bestiary/' + id
                request(link, function (error, response, body) {
                    let result
                    if (!error) {
                        const $ = cheerio.load(body)
                        const name = $('.item-link').text().trim();
                        const size = $('.params .size-type-alignment').text().trim();
                        const ac = $('.params > li:nth-child(2)').contents().filter(function(i, el) {
                            return el.nodeType === 3;
                        }).text().trim();
                        const hitPoints = $('.params > li:nth-child(3)').text().trim();
                        const speed = $('.params > li:nth-child(4)').text().trim();
                        const stats = $('.stats').text().trim();
                        const abilities = $('.subsection:eq(0)').text().trim().replace("Способности", "");
                        const actions = $('.subsection:eq(1)').text().trim().replace("Действия", "");
                        const description = $('.subsection:eq(2)').text().trim().replace("Описание", "");

                        result = {
                            id,
                            link,
                            name,
                            size,
                            ac,
                            hitPoints,
                            speed,
                            abilities,
                            actions,
                            description,
                            stats
                        }
                    } else {
                        console.log("Произошла ошибка: " + error);
                        return promiseReject()
                    }

                    return promiseResolve(result)
                });

                return resp


                // const response = await request(link);
                // const html = cheerio.load(response)
                // const name = html.querySelector('.item-link').text

                // console.log(html);
            }
        )

        return await Promise.all(responses)
    }

